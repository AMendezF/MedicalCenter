	private void actualizarBoton() {
		if (!gestor.esTexto(fieldNombre.getText()) || !gestor.esTexto(fieldApellidos.getText())) {
			buttonActualizarDatos.setEnabled(false);
		} else {
			buttonActualizarDatos.setEnabled(true);
		}
	}

